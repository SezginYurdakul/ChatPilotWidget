export function createI18n(lang = 'en') {
  let currentLang = translations[lang] ? lang : 'en'

  return {
    t(key) {
      return translations[currentLang]?.[key] || translations.en[key] || key
    },
    setLanguage(lang) {
      if (translations[lang]) {
        currentLang = lang
      }
    },
    getLanguage() {
      return currentLang
    }
  }
}
